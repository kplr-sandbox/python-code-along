# Création d'une liste
ma_liste = ['un','deux','trois',4,5]

# Afficher l'élément à l'index 0
print("Result :",ma_liste[0])

# Afficher le reste de la liste
print("Result :",ma_liste[1:])

# faire la jointure de deux liste
print("Result :",ma_liste[:3]+['quatre'])

# Dupliquer le contenu d'une liste
print("Result :",ma_liste[2:]*2)

# Création d'une tuple
ma_tuple = ('un','deux','trois',4,5)

# Afficher l'élément à l'index 0
print("Result :",ma_tuple[0])

# Afficher le reste de la liste
print("Result :",ma_tuple[1:])